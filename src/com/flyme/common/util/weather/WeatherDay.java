package com.flyme.common.util.weather;

/**
 * 一周天气状况
 */
public class WeatherDay {
	private String temp;
	private String tempF;
	private String weather;
	private String img1;
	private String img2;
	private String img_title1;
	private String img_title2;
	
	private String wind;
	private String fx;
	private String fl;
	private String st;
	public String getTemp() {
		return temp;
	}
	public void setTemp(String temp) {
		this.temp = temp;
	}
	public String getTempF() {
		return tempF;
	}
	public void setTempF(String tempF) {
		this.tempF = tempF;
	}
	public String getWeather() {
		return weather;
	}
	public void setWeather(String weather) {
		this.weather = weather;
	}
	public String getImg1() {
		return img1;
	}
	public void setImg1(String img1) {
		this.img1 = img1;
	}
	public String getImg2() {
		return img2;
	}
	public void setImg2(String img2) {
		this.img2 = img2;
	}
	public String getImg_title1() {
		return img_title1;
	}
	public void setImg_title1(String img_title1) {
		this.img_title1 = img_title1;
	}
	public String getImg_title2() {
		return img_title2;
	}
	public void setImg_title2(String img_title2) {
		this.img_title2 = img_title2;
	}
	public String getWind() {
		return wind;
	}
	public void setWind(String wind) {
		this.wind = wind;
	}
	public String getFx() {
		return fx;
	}
	public void setFx(String fx) {
		this.fx = fx;
	}
	public String getFl() {
		return fl;
	}
	public void setFl(String fl) {
		this.fl = fl;
	}
	public String getSt() {
		return st;
	}
	public void setSt(String st) {
		this.st = st;
	}
}
